DROP SEQUENCE TUSERSEQ;
DROP TABLE TUSER;

CREATE SEQUENCE TUSERSEQ;
CREATE TABLE TUSER (

	SEQ NUMBER PRIMARY KEY,
	NAME VARCHAR2(100) NOT NULL,
	ID VARCHAR2(200) NOT NULL,
	PW VARCHAR2(200) NOT NULL,
	ADDR VARCHAR2(500) NOT NULL,
	EMAIL VARCHAR2(100) NOT NULL,
	PHONE VARCHAR2(18) NOT NULL,
	ROLE VARCHAR2(100) NOT NULL,
	MARRIGE VARCHAR2(2) NOT NULL,
	GENDER VARCHAR2(2) NOT NULL,
	ENABLED VARCHAR2(2) NOT NULL,
	
	CONSTRAINT TUSER_NAME_UQ UNIQUE(NAME),
	CONSTRAINT TUSER_ID_UQ UNIQUE(ID),
	CONSTRAINT TUSER_PW_UQ UNIQUE(PW),
	CONSTRAINT TUSER_PHONE_UQ UNIQUE(PHONE),
	CONSTRAINT TUSER_EMAIL_UQ UNIQUE(EMAIL),
	CONSTRAINT TUSER_MARRIGE_CHECK CHECK (MARRIGE IN('Y','N')),
	CONSTRAINT TUSER_GENDER_CHECK CHECK (GENDER IN('M','F')),
	CONSTRAINT TUSER_ENABLED_CHECK CHECK (ENABLED IN('Y','N'))

);


CREATE SEQUENCE TBOARDSEQ;
CREATE TABLE TBOARD (

	SEQ NUMBER PRIMARY KEY,
	NAME VARCHAR2(100) NOT NULL,
	ID VARCHAR2(200) NOT NULL,
	TITLE VARCHAR2(1000) NOT NULL,
	CONTENT VARCHAR2(4000) NOT NULL,
	EMAIL VARCHAR2(100) NOT NULL,
